    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        Connection con = ConnectionBuilder.getConnection();
        try {
            if (passwordField.getText() != "" & userNameField.getText() != "") {
                if (passwordField.equals(confirmField)) {
                    PreparedStatement pstm = con.prepareStatement("SELECT * FROM SHOPPROFILE VALUES (?,?,?,?,?,?,?,?)");
                    pstm.setString(1, userNameField.getText());
                    pstm.setString(2, passwordField.getText());
                    pstm.setString(3, shopNameField.getText());
                    pstm.setString(4, nameField.getText());
                    pstm.setString(5, surnameField.getText());
                    pstm.setString(6, addressField.getText());
                    pstm.setString(7, telField.getText());
                    pstm.setString(8, emailField.getText());
                    JOptionPane.showMessageDialog(null, "Register Successful.");
                    userNameField.setText("");
                    passwordField.setText("");
                    shopNameField.setText("");
                    nameField.setText("");
                    surnameField.setText("");
                    addressField.setText("");
                    telField.setText("");
                    emailField.setText("");
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_submitButtonActionPerformed

